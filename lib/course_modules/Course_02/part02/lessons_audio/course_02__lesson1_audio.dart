  final String filePath;
  final String label;

  AudioFile({required this.filePath, required this.label});
}

class Course02Part02Lesson01Audio extends StatefulWidget {
  const Course02Part02Lesson01Audio({Key? key}) : super(key: key);

  @override
  State<Course02Part02Lesson01Audio> createState() =>
      _Course02Part02Lesson01AudioState();
}

class _Course02Part02Lesson01AudioState
    extends State<Course02Part02Lesson01Audio> {
  List<AudioPlayer> players = [];
  List<bool> isPlayingList = [];
  List<Duration> durationList = [];
  List<Duration> positionList = [];

  String formatTime(int seconds) {
    return '${Duration(seconds: seconds)}'.split('.')[0].padLeft(8, '0');
  }

  List<AudioFile> audioFiles = [
    AudioFile(
        filePath:
