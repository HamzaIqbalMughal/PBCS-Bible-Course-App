class AudioFile {
  final String filePath;
  final String label;

  AudioFile({required this.filePath, required this.label});
}

class Course02Part02Lesson02Audio extends StatefulWidget {
  const Course02Part02Lesson02Audio({Key? key}) : super(key: key);

  @override
  State<Course02Part02Lesson02Audio> createState() =>
      _Course02Part02Lesson02AudioState();
}

class _Course02Part02Lesson02AudioState
    extends State<Course02Part02Lesson02Audio> {
  List<AudioPlayer> players = [];
  List<bool> isPlayingList = [];
  List<Duration> durationList = [];
  List<Duration> positionList = [];

  String formatTime(int seconds) {
    return '${Duration(seconds: seconds)}'.split('.')[0].padLeft(8, '0');
  }

  List<AudioFile> audioFiles = [
    AudioFile(
        filePath:
            'https://firebasestorage.googleapis.com/v0/b/pbcs-bible-course-81b4b.appspot.com/o/audios%2FCourse02%2FPart02%2FLesson02%2F1.mp3?alt=media&token=de4ec10d-7043-43c7-b114-15ddd6addc60',
        label: 'Abdi badshah almaseeh k alqaab'),
    AudioFile(
